function getColor(rating) {
  if (rating >= 4.5) {
    return 'green';
  } else if (rating >= 4) {
    return 'lime';
  } else if (rating >= 3.5) {
    return 'yellow';
  } else if (rating >= 3) {
    return 'orange';
  } else {
    return 'red';
  }
}

